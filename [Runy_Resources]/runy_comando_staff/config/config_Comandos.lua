--[[ SIGA O MESMO PADRÃO ABAIXO PARA ALTERAR OS COMANDOS ]]


-- Comando para entrar em Atendimento
cmd_atendimento = 'sa'

-- Modo de Uso: /comando ID
-- serve para deixar o jogador imóvel mesmo se estiver dentro do veiculo
cmd_congelar = 'congelar'

-- Modo de Uso: /comando ID do veiculo
-- Serve para spawnar um veiculo a partir do ID dele
-- Use esse site para encontrar todos ids: ( https://wiki.multitheftauto.com/wiki/Vehicle_IDs )
cmd_spawnar_veiculo = 'veh'

-- Modo de Uso: /comando ID
-- Serve para desbugar um jogador
cmd_desbugar = 'desbugar'

-- Serve para destruir todos os carros desocupados do servidor
cmd_resetar_veiculos = 'rescar'

-- Modo de Uso: /comando ID da Pessoa, Quantidade de Gasolina
-- Serve para spawnar um veiculo a partir do ID dele
cmd_setar_gasolina = 'setgas'

-- Modo de Uso: /comando ID da pessoa, ID da Skin
-- Os ID das skin pode ser encontrado nesse site: ( https://wiki.multitheftauto.com/wiki/All_Skins_Page )
cmd_setar_skin = 'ss'

-- Usado para VOAR na Cidade.
cmd_fly = 'fly'

-- Modo de Uso: /comando ID da pessoa, Quantidade de VIDA
-- Serve para setar a quantidade de VIDA da pessoa
cmd_setvida = 'setvida'

-- Modo de Uso: /comando ID da pessoa
-- Serve para consertar o veiculo de alguem. OBS: A Pessoa precisa estar dentro do veiculo.
cmd_consertar_veh = 'fix'

-- Modo de Uso: /comando ID da pessoa
-- Serve para DESTRUIR o veiculo de alguem. OBS: A Pessoa precisa estar dentro do veiculo.
cmd_destruir_veh = 'dv'

-- Modo de Uso: /comando ID da pessoa, Quantidade de Colete
-- Serve para setar a quantidade de COLETE da pessoa
cmd_setar_colete = 'setcolete'

-- Modo de Uso: /comando ID da pessoa, Quantidade de FOME e SEDE
-- Serve para setar a quantidade de FOME e SEDE da pessoa
cmd_setar_fome = 'setfome'

-- Modo de Uso: /comando ID da pessoa
-- Serve para teleportar até alguma pessoa
cmd_tp = 'tp'

-- Modo de Uso: /comando ID da pessoa
-- Serve para puxar alguma pessoa até você
cmd_puxar_player = 'puxar'

-- Modo de Uso: /comando ID da Pessoa
-- Serve para dar TP dentro do veiculo de Alguem
cmd_teleport_veh = 'tpveh'

-- Modo de Uso: /comando ID da Pessoa
-- Serve para puxar alguma pessoa com o veiculo junto.
cmd_puxarveh = 'puxarveh'